<section class="px-4 pb-8 leading-relaxed centered-content">
	<div class="py-4">
		<h2 class="text-xl font-semibold">How to actually get good at managing your time</h2>
	</div>
	<p>Disclaimer: Read all of my blog posts with a grain of salt.</p>
	<p>
		As someone who likes solving problems, I think time management is a unique and interesting
		challenge. Here's some things I learned that helped me manage my time better.
	</p>
	<h3 class="text-lg font-medium">Define "good"</h3>
	<p>
		The first step to fixing bad time management is defining what good time management looks like.
		In high school, I wanted to:
	</p>
	<ol class="indent-16">
		<li>a&rpar; Spend the minimum amount of time on studying to get good grades (&ge;96)</li>
		<li>b&rpar; Fulfill my responsibilities in school clubs (computer science club, math club)</li>
		<li>c&rpar; Spend lots of time on extracurriculars</li>
		<li>d&rpar; Spend lots of time on coding projects</li>
		<li>e&rpar; Sleep &ge;8 hours a day</li>
		<li>f&rpar; Still have some time to exercise, socialize, and have fun</li>
	</ol>
	<p>In university, my goals are similar but different.</p>
	<ol class="indent-16">
		<li>a&rpar; Spend the minimum amount of time on studying to get decent grades (&ge;80)</li>
		<li>a&rpar; Spend lots of time applying to jobs, polishing my resume</li>
		<li>d&rpar; Spend lots of time on coding projects</li>
		<li>f&rpar; Regularly exercise, socialize, and have fun</li>
		<li>e&rpar; Sleep &ge;8 hours a day</li>
	</ol>
	<p>
		I believe that listing down your goals immediately makes them more achievable. After all, you
		can't succeed at anything if you don't have a clear definition of what success looks like!
	</p>
	<h3 class="text-lg font-medium">Analyzing the difference</h3>
	<p>
		So, you've decided what "good" time management looks like. Take the time to analyze the
		difference between the quality (or lack thereof) of your current time management and your goal.
	</p>
	<p>
		If you often feel like you don't even know where your time is going, (or simply don't want to
		admit it), try tracking your screen time usage (because, after all, that's likely where all your
		time is going).
	</p>
	<p>Spending too much time on Instagram and Reddit?</p>
	<p>Spending too much time on gaming?</p>
	<p>Spending too much time on... something else that isn't productive?</p>
</section>

